-- creates/updates a question list for already existing questions
create or replace function upsert_custom_question_list(
    p_question_list_id bigint,
    p_author varchar(255),
    p_questions jsonb
)
    returns int
    language plpgsql
as $$
declare
    v_question_list_id int;
begin
    if p_questions is null or jsonb_typeof(p_questions) <> 'array' then
        raise exception 'p_questions must be a JSONB array of objects like {"id": 1, "params": {}} TODO! put schema name here';
    end if;

    if p_question_list_id = current_setting('esup_41_custom_questions.default_list_id')::bigint then
        raise exception 'Cannot update default question list';
    end if;

    if p_question_list_id is null then
        insert into question_list(author)
        values (p_author)
        returning id into v_question_list_id;
    else
        v_question_list_id := p_question_list_id;

        update question_list
        set author = coalesce(p_author, author)
        where id = v_question_list_id;
    end if;

    delete from question_list_item
    where question_list_id = v_question_list_id;

    -- insert in the order of the JSON array
    insert into question_list_item(
        question_list_id,
        question_id,
        "position",
        params
    )
    select
        v_question_list_id,
        (q_elem->>'id')::int as question_id,
        q_ord as "position",
        coalesce(q_elem->'params', '{}'::jsonb) as params
    from jsonb_array_elements(p_questions) with ordinality as x(q_elem, q_ord)
    where q_elem ? 'id';

    return v_question_list_id;
end;
$$;

