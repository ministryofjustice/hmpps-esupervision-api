create or replace function get_question_templates(p_lang text_language, p_author varchar(255))
    returns table (
                      question_id bigint,
                      question_template text,
                      response_format response_format,
                      response_spec jsonb
                  ) as $$
begin
    return query
        select
            q.id,
            qi.question_template, qi.response_format, qi.response_spec
        from question q
        join question_info qi on q.id = qi.question_id
        where
            qi.lang = p_lang
          and q.author = p_author
          and q.policy = 'CUSTOMISABLE'::question_policy
        order by q.created_at;
end;
$$ LANGUAGE plpgsql STABLE;

