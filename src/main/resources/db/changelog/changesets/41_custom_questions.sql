create or replace function validate_question_localisations()
    returns trigger
    language plpgsql
as $$
declare
    q_id bigint;
    lang_count int;
    has_en boolean;
    has_cy boolean;
begin
    q_id := coalesce(new.id, old.id);

    select
        count(*)::int,
        bool_or(lang = 'en-GB'),
        bool_or(lang = 'cy-GB')
    into
        lang_count,
        has_en,
        has_cy
    from question_info qi
    where qi.question_id = q_id;

    if new.author = 'SYSTEM' and lang_count <> 2 or not has_en or not has_cy then
        raise exception
            'question % must have exactly two translations: en-GB and cy-GB',
            q_id;
    end if;

    return null;
end;
$$;

create constraint trigger question_localisation_check_from_question
    after insert or update of author
    on question
    deferrable initially deferred
    for each row
execute function validate_question_localisations();

