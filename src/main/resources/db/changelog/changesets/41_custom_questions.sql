create or replace function define_system_question(
    p_response_format response_format,
    en_question_template text,
    en_spec jsonb,
    cy_question_template text,
    cy_spec jsonb
) returns bigint as $$
declare
    new_question_id bigint;
begin
    insert into question (author)
    values ('SYSTEM')
    returning id into new_question_id;

    insert into question_info (
        question_id,
        lang,
        question_template,
        response_format,
        response_spec
    )
    select
        new_question_id,
        q.lang,
        q.template,
        p_response_format,
        q.spec
    from (
             values
                 ('en-GB'::text_language, en_question_template, en_spec),
                 ('cy-GB'::text_language, cy_question_template, cy_spec)
         ) as q(lang, template, spec);

    return new_question_id;
end;
$$ language plpgsql;

